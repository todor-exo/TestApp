    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            PrintWriter fPrn = new PrintWriter(new FileOutputStream("data.txt",false),true);
            for (int i=0;i<TestApp.arrStSize;i++){
                String s = String.format( new Locale("Locale.US"), "%02d %s %s %.2f\n", i+1,((Student)(TestApp.arrStud[i])).getFName(), ((Student)(TestApp.arrStud[i])).getLName(),((Student)(TestApp.arrStud[i])).getMark());
                fPrn.print(s);
            }
            fPrn.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(StudentForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
         try {
            FileInputStream fstream = new FileInputStream("data.txt");            
            DataInputStream in = new DataInputStream(fstream);           
            BufferedReader fBr = new BufferedReader(new InputStreamReader(in));   
            String s;             
            try {            
                TestApp.arrStSize = 0;
                while ((s = fBr.readLine()) !=  null){
                    String sss[] = s.split(" ");
                    Student st = new Student(sss[1], sss[2], Float.parseFloat(sss[3]));
                    TestApp.arrStud[TestApp.arrStSize] = st;                                            
                    TestApp.arrStSize++;
                }            
            } catch (IOException ex) {
                Logger.getLogger(StudentForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            populateData();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(StudentForm.class.getName()).log(Level.SEVERE, null, ex);  
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int num = Integer.parseInt(jTextField4.getText());      
        
        if (num > TestApp.arrStud.length){                 
            JOptionPane.showMessageDialog(this, String.format(new Locale("Locale.US"), "Num should be <= %d", TestApp.arrStud.length));
            return;  
        }
        
        for(int i=num;i<TestApp.arrStSize;i++){
            TestApp.arrStud[i-1] = TestApp.arrStud[i];
        }        
        if (TestApp.arrStSize > 0){
            TestApp.arrStSize--;
        }
        populateData();
    }//GEN-LAST:event_jButton2ActionPerformed

