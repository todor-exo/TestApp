    private void populateData(){
        jTextArea1.setText("");
        for (int i=0;i<TestApp.arrStSize;i++){
            String s = String.format( new Locale("Locale.US"), "%02d %s %s %.2f\n", i+1,((Student)(TestApp.arrStud[i])).getFName(), ((Student)(TestApp.arrStud[i])).getLName(),((Student)(TestApp.arrStud[i])).getMark());
            jTextArea1.append(s);
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        float m = 0;
        try {
            m = Float.parseFloat(jTextField3.getText());
        }
        catch (NumberFormatException ex){
            m =0;
        }
        Student st = new Student(jTextField1.getText(), jTextField2.getText(), m);
        int num = Integer.parseInt(jTextField4.getText());              
        if (num > TestApp.arrStud.length){                 
            JOptionPane.showMessageDialog(this, String.format(new Locale("Locale.US"), "Num should be <= %d", TestApp.arrStud.length));
            return;  
        }
        TestApp.arrStud[num-1] = st;

        if (TestApp.arrStSize < num){    
            TestApp.arrStSize++;            
        }                        
        String s = String.format( new Locale("Locale.US"), "%02d %s %s %.2f\n", num,((Student)(TestApp.arrStud[num-1])).getFName(), ((Student)(TestApp.arrStud[num-1])).getLName(),((Student)(TestApp.arrStud[num-1])).getMark());
        jTextArea1.append(s);
        
    }//GEN-LAST:event_jButton1ActionPerformed

